import ts from 'typescript';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const STEPS_DIR = path.join(ROOT, 'steps');
const OUT_DIR = path.join(ROOT, '.vscode', 'cucumber-glue');
const KEYWORDS = ['Given', 'When', 'Then'];

function extractSteps(filePath) {
  const source = fs.readFileSync(filePath, 'utf8');
  const sourceFile = ts.createSourceFile(filePath, source, ts.ScriptTarget.Latest, true, ts.ScriptKind.TS);
  const stepsByKeyword = { Given: [], When: [], Then: [] };

  function visit(node) {
    if (
      ts.isCallExpression(node) &&
      ts.isIdentifier(node.expression) &&
      KEYWORDS.includes(node.expression.text) &&
      node.arguments.length > 0 &&
      ts.isStringLiteral(node.arguments[0])
    ) {
      stepsByKeyword[node.expression.text].push(node.arguments[0].text);
    }
    ts.forEachChild(node, visit);
  }
  visit(sourceFile);
  return stepsByKeyword;
}

function toGlueFileName(stepsFileName) {
  return stepsFileName.replace(/\.steps\.ts$/, '.steps.js');
}

function renderGlueFile(sourceFileName, stepsByKeyword) {
  const usedKeywords = KEYWORDS.filter((k) => stepsByKeyword[k].length > 0);
  const lines = [
    '/*',
    ` * AUTO-GENERATED by scripts/generate-cucumber-glue.mjs — do not edit by hand.`,
    ' * IDE-only step map so the "Cucumber (Official)" VS Code extension can match',
    ` * steps defined in steps/${sourceFileName} (playwright-bdd's real runtime never`,
    ' * loads this file — see ai/playwright-bdd-style.md).',
    ' * Regenerate with: npm run generate:cucumber-glue',
    ' */',
    '',
    `import { ${usedKeywords.join(', ')} } from '@cucumber/cucumber';`,
    '',
  ];
  for (const keyword of KEYWORDS) {
    for (const text of stepsByKeyword[keyword]) {
      lines.push(`${keyword}(${JSON.stringify(text)}, function () {});`);
    }
  }
  lines.push('');
  return lines.join('\n');
}

function main() {
  const stepFiles = fs.readdirSync(STEPS_DIR).filter((f) => f.endsWith('.steps.ts'));
  fs.rmSync(OUT_DIR, { recursive: true, force: true });
  fs.mkdirSync(OUT_DIR, { recursive: true });

  let totalSteps = 0;
  for (const fileName of stepFiles) {
    const stepsByKeyword = extractSteps(path.join(STEPS_DIR, fileName));
    const count = KEYWORDS.reduce((sum, k) => sum + stepsByKeyword[k].length, 0);
    if (count === 0) continue;
    totalSteps += count;
    const outPath = path.join(OUT_DIR, toGlueFileName(fileName));
    fs.writeFileSync(outPath, renderGlueFile(fileName, stepsByKeyword));
  }

  console.log(`Generated IDE glue for ${totalSteps} step definitions in ${OUT_DIR}`);
}

main();
